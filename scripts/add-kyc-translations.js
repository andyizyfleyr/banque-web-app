files.forEach(file => {
    const filePath = path.join(translationsDir, file);
    const lang = file.replace('.json', '');
    const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));

    data.kyc = kycKeys[lang] || kycKeys['en'];

    if (!data.kyc.required) {
        data.kyc.required = data.kyc.verifiedMsg ? "Veuillez finaliser votre vérification d'identité pour accéder à cette fonctionnalité." : "Please finalize your identity verification to access this feature.";
        if (lang === 'en') data.kyc.required = "Please finalize your identity verification to access this financial feature.";
        if (lang === 'es') data.kyc.required = "Por favor, finalice su verificación de identidad para acceder a esta función financiera.";
        if (lang === 'de') data.kyc.required = "Bitte schließen Sie Ihre Identitätsüberprüfung ab, um auf diese Finanzfunktion zugreifen zu können.";
        if (lang === 'it') data.kyc.required = "Si prega di completare la verifica dell'identità per accedere a questa funzione finanziaria.";
        if (lang === 'pt') data.kyc.required = "Por favor, finalize sua verificação de identidade para acessar este recurso financeiro.";
        if (lang === 'ro') data.kyc.required = "Vă rugăm să finalizați verificarea identității pentru a accesa această funcție financiară.";
        if (lang === 'pl') data.kyc.required = "Proszę sfinalizować weryfikację tożsamości, aby uzyskać dostęp do tej funkcji finansowej.";
        if (lang === 'el') data.kyc.required = "Παρακαλώ ολοκληρώστε την επαλήθευση της ταυτότητάς σας για πρόσβαση σε αυτή τη λειτουργία.";
    }

    fs.writeFileSync(filePath, JSON.stringify(data, null, 4));
});
